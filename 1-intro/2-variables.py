# A variable is a little container that stores an information.
# An information can be of different types.
# An integer number: 42
# A real number:     4.2345
# A string:          "hello world!"
# A boolean (True or False)
# And many others...

# Each variable has a name which can be used to easily access its content.
# For example:

my_integer_variable = 42   # Create a variable with a name.
print(my_integer_variable) # Show the value of the variable (42).

my_integer_variable = 7    # Change the value of the variable.
print(my_integer_variable) # Show the new value of the variable (7).

another_variable = 4.2345  # Create another variable.
print(another_variable)

string_variable = "hello world!"
print(string_variable)

boolean_variable = True
print(boolean_variable)
